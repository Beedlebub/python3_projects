# Initialize our two variables
a = 8
b = 4

# Display starting values for both varaibles
print('\nAssign Values:\t\t', 'a =', a, '\tb =', b)

# Add and assign a new value to the first variable
a += b
print('Add & Assign:\t\t', 'a =', a, '(8 += 4)')

# Subtract and assign a new value to the first variable
a -= b
print('Subtract & Assign:\t', 'a =', a, '(12 -= 4)')

# Multiply and assign a new value to the first variable
a *= b
print('Multiply & Assign:\t', 'a =', a, '(8 *= 4)')

# Divide and assign a new value to the first variable
a /= b
print('Divide & Assign:\t', 'a =', a, '(32 /= 4)')

# Modulus and assign a new value to the first variable
a %= b
print('Modulus & Assign:\t', 'a =', a, '(8 *= 4)', '\n')
