# Initialize two variables with Boolean values for logical evaluation
a = True
b = False

# Display the results of logical AND evaluations
# Remember, two wrongs do not make a right!!
print('AND Logic:')
print('a and a =', a and a)
print('a and b =', a and b)
print('b and b =', b and b)

# Display results of logical OR evaluations
print('\nOR Logic:')
print('a or a =', a or a)
print('a or b =', a or b)
print('b or b =', b or b)

# Display results of logical NOT evaluations
print('\nNOT Logic:')
print('a = ', a, '\tnot a = ', not a)
print('b = ', b, '\tnot b = ', not b, '\n')
