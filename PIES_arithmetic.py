# Initialize our two variables
a = 8
b = 2

# Display the result of adding the varaiable values
print('\nAddition:\t', a, '+', b, '=', a + b)

# Display the result of subtracting the varaiable values
print('Subtraction:\t', a, '-', b, '=', a - b)

# Display the result of multiplying the varaiable values
print('Multiplication:\t', a, 'x', b, '=', a * b)

# Display the result of diving the varaiable values,
# both with and without floating point remainders
print('Division:\t', a, '/', b, '=', a / b)
print('Floor Division:\t', a, '//', b, '=', a // b)

# Display the remainder after diving the two values
print('Modulus:\t', a, '%', b, '=', a % b)

# Display the result of rasing first operand by the second
print('Exponent:\t', a, 'squared', '=', a ** b, '\n')
