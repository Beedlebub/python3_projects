# Initialize our variables
nil = 0
num = 0
max = 1
cap = 'A'
low = 'a'

# Display results of numeric and character eqaulity comparisons
print('\nEquality :\t', nil, '==', num, nil == num)
print('Equality :\t', cap, '==', low, cap == low)

# Display the result of an ineqaulity comparison
print('Inequality :\t', nil, '!=', max, nil != max)

# Display the results of greater and lesser comparisons
print('Greater :\t', nil, '>', max, nil > max)
print('Lesser :\t', nil, '<', max, nil < max)

# Display the results of greater or equal, and lesser or equal comparisons
print('More or Equal :\t', nil, '>=', num, nil >= num)
print('Less or Equal :\t', max, '<=', num, max <= num, '\n')
